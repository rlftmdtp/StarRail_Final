package starrail.payment.domain;

public class CardCompanyVO {

	private int card_id;
	private String card_company;
	private String card_num;
	private int card_cvc;
	private String card_pw;
	private int card_month;
	private int card_year;
	private String m_name;
	
	public int getCard_id() {
		return card_id;
	}
	public void setCard_id(int card_id) {
		this.card_id = card_id;
	}
	public String getCard_company() {
		return card_company;
	}
	public void setCard_company(String card_company) {
		this.card_company = card_company;
	}
	public String getCard_num() {
		return card_num;
	}
	public void setCard_num(String card_num) {
		this.card_num = card_num;
	}
	public int getCard_cvc() {
		return card_cvc;
	}
	public void setCard_cvc(int card_cvc) {
		this.card_cvc = card_cvc;
	}
	public String getCard_pw() {
		return card_pw;
	}
	public void setCard_pw(String card_pw) {
		this.card_pw = card_pw;
	}
	public int getCard_month() {
		return card_month;
	}
	public void setCard_month(int card_month) {
		this.card_month = card_month;
	}
	public int getCard_year() {
		return card_year;
	}
	public void setCard_year(int card_year) {
		this.card_year = card_year;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
	
	
}
