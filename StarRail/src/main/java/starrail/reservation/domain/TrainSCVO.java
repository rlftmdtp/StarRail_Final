package starrail.reservation.domain;

public class TrainSCVO {

	private String trainType;
	private String train_stime;
	private String train_etime;
	
	
	public String getTrainType() {
		return trainType;
	}
	public void setTrainType(String trainType) {
		this.trainType = trainType;
	}
	public String getTrain_stime() {
		return train_stime;
	}
	public void setTrain_stime(String train_stime) {
		this.train_stime = train_stime;
	}
	public String getTrain_etime() {
		return train_etime;
	}
	public void setTrain_etime(String train_etime) {
		this.train_etime = train_etime;
	}
	
	
}
